'''
숫자형. ( Number. 숫자 형태로 이루어진 자료형 )
ex. 정수, 실수, 8진수, 16진수

정수형. ( Integer. 정수를 뜻하는 자료형 )
a = 123 # 양의 정수
a = -178 # 음의 정수
a = 0 # 숫자 0
print(a)

실수형. ( floating-point. 소수점이 포함된 숫자 )
a = 1.2
a = -3.45

# 컴퓨터식 지수 표현 방식. ( e 와 E 둘 중 어느 것을 사용해도 된다 )
a = 4.24E10
a = 4.24e-10

# 8진수. ( octal. 0o or 0O 로 시작 )
a = 0o177 # 1 x 8의 제곱 + 7 x 8 + 7 = 127

# 16진수. ( hexadecimal. 0x 로 시작 )
a = 0xABC # 10 x 16의 제곱 + 11 x 16 + 12 = 2748 ( A: 10, B: 11, C:12 )
'''
print(a)